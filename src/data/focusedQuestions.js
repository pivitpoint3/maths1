// Get questions for a specific focus
export function getFocusedQuestions(focus, subtopic = null) {
  if (focus === 'timesTables' && subtopic) {
    // Convert table number (2-12) to key (twos-twelves)
    const tableKeys = ['', '', 'twos', 'threes', 'fours', 'fives', 'sixes', 'sevens', 'eights', 'nines', 'tens', 'elevens', 'twelves']
    const key = tableKeys[subtopic]
    return focusedQuestions.timesTables[key] || []
  }
  return focusedQuestions[focus] || []
}

// Shuffle array
function shuffleArray(arr) {
  return [...arr].sort(() => Math.random() - 0.5)
}

// Get shuffled questions
export function getShuffledFocusedQuestions(focus, subtopic = null) {
  const questions = getFocusedQuestions(focus, subtopic)
  return shuffleArray(questions)
}
